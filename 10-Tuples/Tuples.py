# Declaration of a few tuples
myInts = (1, 2, 3, 4)
myFloats = (1.0, 2.0, 3.0, 4.0)
myStrings = ("Hi", "Hey", "Hello", "Howdy")
myBooleans = (True, False, True, False)
myDataTypes = (7, 23.0, "Greetings", True)

# Printing out of the tuples
print(myInts)
print(myFloats)
print(myStrings)
print(myBooleans)
print(myDataTypes)
print()

# Printing out of the length of a tuple
print(myDataTypes)
print(len(myDataTypes))
print()

# Printing out of individual items in a tuple
print(myDataTypes[0])
print(myDataTypes[1])
print(myDataTypes[2])
print(myDataTypes[3])
print()

# Indexing and counting a tuple
myTuple = (1, 1, 1, 2, 2, 3)
print(myTuple)
print(myTuple.count(1))
print(myTuple.index(2))
print()
# Conditional statement with if execution
if (1 > 0):
    print("True")
elif (1 < 0):
    print("False")
else:
    print("Neither")

#Conditional statement with elif execution
if (0 > 1):
    print("True")
elif (0 < 1):
    print("False")
else:
    print("Neither")

# Conditional statement with else execution
if (0 > 1):
    print("True")
elif (1 < 0):
    print("False")
else:
    print("Neither")